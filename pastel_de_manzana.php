        <h2 id="ingredientes">Ingredientes</h2>
        <hr>
        <ul>
            <li>3 manzanas rojas</li>
            <li>¾ taza de aceite de coco derretido</li>
            <li>3 huevos a temperatura ambiente</li>
            <li>2 tazas harina de trigo regular</li>
            <li>¾ taza azúcar mascabado</li>
            <li>1 ½ cucharada polvo de hornear</li>
            <li>1 cucharita canela molida</li>
            <li>1 limón grande</li>
            <li>Azúcar glass</li>
        </ul>
    </div>

    <div>
        <h2 id="pasos">Pasos para praparar los churros</h2>
        <hr>
        <div style="text-align: justify; font-size: 20px;">
            <ol>
                <li class="mb-2">Precalienta el horno a 180ºC.</li>
                <li class="mb-2">Pela las manzanas. Corta 2 manzanas en cubos chicos y cúbrelas con el jugo de limón.</li>
                <li class="mb-2">La manzana restante corta en cubos grandes y colócala en la licuadora junto con el aceite, canela, los huevos y el azúcar. Es importante que los huevos no estén fríos o solidificarán al aceite de coco.</li>
                <li class="mb-2">Licua muy bien y vacía la mezcla a un bowl.</li>
                <li class="mb-2">Cierne la harina y polvo de hornear y agrega a la mezcla. Incorpora suavemente con un miserable para envolver, sin mezclar de más o no quedará ligero y esponjoso el pan.</li>
                <li class="mb-2">Por último, agrega los cubos de manzana e incorpora.</li>
                <li class="mb-2">Vacía en un molde engrasado y hornea 40 a 45 minutos o hasta que al insertar un palillo este salga completamente limpio. Si no está listo a ese tiempo ve agregando 5 minutos hasta que el palillo salga limpio. El tiempo depende de que tan calibrado esté tu horno, recuerda que todos los hornos son distintos.</li>
                <li class="mb-2">Deja reposar 5 minutos en el molde.</li>
                <li class="mb-2">Desmolda con cuidado y decora con azúcar glass si lo prefieres.</li>
                <li>De preferencia consume el mismo día ya que su sabor es más rico. Si te sobra lo mejor es refrigerarlo (sobre todo si vives en un lugar muy caliente).</li>
                <li>Puedes colocar los churros en un recipiente y colocar en un horno tibio mientras terminas de preparar todos.</li>
            </ol>
