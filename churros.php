        <hr>
        <ul>
            <li>80 gr mantequilla en cuadros</li>
            <li>⅔ taza agua</li>
            <li>⅔ taza harina de trigo</li>
            <li>1 pizca de sal</li>
            <li>¼ cucharita canela molida</li>
            <li>2 cucharadas azúcar blanca refinada</li>
            <li>2 huevos</li>
        </ul>
