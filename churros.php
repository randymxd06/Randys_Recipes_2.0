                <p>El origen de los churros es desconocido. Una teoría dice que fueron exportados desde China hacia Europa, por los portugueses. Entre las nuevas técnicas culinarias que trajeron, se incluyó la modificación de la masa de youtiao, también conocido como youzhagui en el sur de China. Esta modificación tiene que ver con el diseño de la masa, ya que no se utilizó la técnica tradicional de "tirar" la masa, sino que se produjeron a través de la forma "estrella", que requiere del uso de una manga con esa forma.</p>
