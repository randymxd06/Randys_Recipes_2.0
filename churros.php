    <div>
        <h2 id="pasos">Pasos para praparar los churros</h2>
        <hr>
        <div style="text-align: justify; font-size: 20px;">
            <ol>
                <li class="mb-2">Derrite la mantequilla junto con el agua en una cacerola mediana.</li>
                <li class="mb-2">Baja el fuego, y agrega la harina, sal, canela y azúcar.</li>
                <li class="mb-2">Mezcla bien con una cuchara de madera hasta que forme una bola, entre 40 segundos a 70 segundos. Revisa el video para que veas la textura.</li>
                <li class="mb-2">Retira del fuego y agrega los huevos uno por uno hasta que esté bien mezclado. Puedes hacer esto en la batidora con la paleta o pala. Bate muy bien y cuando se vea tersa y suave la mezcla vacíala a la una manga pastelera con la duya de estrella o bien una churrera.</li>
                <li class="mb-2">Calienta el aceite en una olla o freidora hasta los 180°C. Si está muy caliente los churros se van a dorar por fuera, pero van a quedar crudos por dentro. Puedes hacer una prueba con un poco de la mezcla, si flota y empieza a burbujear es que ya está listo.</li>
                <li class="mb-2">Aplasta la manga para dejar caer masa en el aceite (con cuidado) y para cortar los churros o que deje de salir puedes dejar de presionar o cortar la masa con unas tijeras o cuchillo.</li>
                <li class="mb-2">Cocina hasta que queden dorados por igual. Si los haces más grandes tienes que cocinar un poco más en el aceite para que se cocinen por completo y no queden crudos por dentro.</li>
                <li class="mb-2">Escurre los churros y coloca en papel absorbente. Cubre con la mezcla de azúcar & canela cuando aún estén tibios o calientes para que se les pegue bien. Cubre bien y sirve calientes, tibios o como más se te antojen acompañados de la salsa de chocolate.</li>
                <li>Puedes colocar los churros en un recipiente y colocar en un horno tibio mientras terminas de preparar todos.</li>
            </ol>
