        <h1 class="mt-5" id="descripcion">Churros</h1>
        <hr>
        <p>El churro es una masa a base de harina cocinada en aceite, una comida de las denominadas «frutas de sartén». Los churros son populares en España, Portugal, Francia, América Latina, Filipinas, Bélgica y algunas zonas de los Estados Unidos. La fabricación y venta se realiza en locales especializados denominados churrerías o fábricas de churros.</p>
        <p>Sus ingredientes básicos son agua, harina (de trigo generalmente, aunque puede ser de otro origen), aceite y sal.</p>
        <p>Su forma puede ser recta, en forma de bastón, en lazos o rulos (espirales). En sus variantes modernas, a veces pueden tener relleno o estar rebozados de azúcar, chocolate, crema pastelera o dulce de leche. Suelen consumirse en el desayuno o en la merienda, junto con una taza de chocolate caliente o café con leche, o acompañados con mate, en el Río de la plata.</p>
        <p>Un churro consiste en una masa compuesta por harina, agua, y sal. Una vez hecha la masa, esta se coloca en un aparato cilíndrico similar a una manga pastelera y se empuja con un pistón sobre una boquilla por donde sale mediante extrusión, y con sección trasversal en forma de estrella. Finalmente, se fríen en aceite y una vez hechos, a veces, se rebozan en azúcar.</p>
        <p>La forma estrellada es necesaria porque, al freír el churro, el aceite sella la superficie exterior, mientras que la masa crece mucho con el calor. Si no tuviera esta forma, que aumenta la superficie sellada, estallaría al expandirse la masa. En la actualidad en los casos en los que se emplea aceite de oliva, este se suele mezclar con girasol o soja para reducir gastos. El aceite de soja no se emplea debido a la gran cantidad de humo que produce, sin embargo el aceite de girasol (sin mezclar) se utiliza con mayor frecuencia en las churrerías debido a su menor coste.</p>
